/**
 * Get all icons.
 *
 * @return {Object} All icons.
 */
export const getAllIcons = () => icons;

/**
 * Get icon SVG by name.
 *
 * @param {string} name Icon name.
 * @return {string|null} Icon SVG string or null if not found.
 */
export const getIconSvg = ( name ) => {
	return icons[ name ]?.svg || null;
};

/**
 * Get icons by category.
 *
 * @param {string} category Category name (optional, returns all if not provided).
 * @return {Array} Array of icon names.
 */
export const getIconsByCategory = ( category = null ) => {
	if ( ! category ) {
		return Object.keys( icons );
	}
	return Object.entries( icons )
		.filter( ( [ , data ] ) => data.category === category )
		.map( ( [ name ] ) => name );
};

/**
 * Search icons by name.
 *
 * @param {string} query Search query.
 * @return {Array} Array of matching icon names.
 */
export const searchIcons = ( query ) => {
	const normalizedQuery = query.toLowerCase();
	return Object.keys( icons ).filter( ( name ) =>
		name.toLowerCase().includes( normalizedQuery )
	);
};

export default icons;
